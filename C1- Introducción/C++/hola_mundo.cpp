// Hola Mundo en C++
// Este programa imprime "Hola Mundo" en la pantalla.
// Es el programa más básico que se escribe al iniciar con cualquier lenguaje de programación.

// El programa utiliza la biblioteca estándar de C++ para la entrada y salida de datos.
#include <iostream> 

// La función principal del programa, donde comienza la ejecución
int main() {

	std::cout << "Hola Mundo" << std::endl; // Imprime "Hola Mundo" en la consola
	// std::endl es un manipulador de flujo que inserta un carácter de nueva línea y vacía el búfer de salida.
	// std::cout es el flujo de salida estándar en C++.
	// std::cout es parte del espacio de nombres std, que contiene todas las funciones y objetos estándar de C++.

    return 0; // Indica que el programa terminó correctamente
}



